CREATE TABLE LIBROS (
  ID     NUMBER GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
  TITULO VARCHAR2(255 CHAR) NOT NULL,
  AUTOR  VARCHAR2(255 CHAR) NOT NULL,
  ANIO   VARCHAR2(4 CHAR),
  GENERO VARCHAR2(20 CHAR)  NOT NULL
);

ALTER TABLE LIBROS ADD CONSTRAINT CK_LIBROS_GENERO
  CHECK (GENERO IN ('FICCION','NO_FICCION','FANTASIA','TERROR','CIENCIA','BIOGRAFIA','POESIA','INFANTIL','COMICS','OTRO'));